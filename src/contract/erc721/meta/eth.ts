class Eth extends EthContract implements Erc721 {

    constructor(address: string) {
        super(address, ABI);
    }

    totalSupply = async (): Promise<number> => {
        return await this.contract.methods.totalSupply().call()
    }
    balanceOf = async (owner: string): Promise<number> => {
        return await this.contract.methods.balanceOf(owner).call()
    }
    ownerOf = async (tokenId: string): Promise<string> => {
        return await this.contract.methods.ownerOf(tokenId).call()
    }
    safeTransferFrom = async (from: string, to: string, tokenId: string): Promise<string> => {
        return await this.contract.methods.safeTransferFrom(from, to, tokenId).encodeABI()
    }
    transferFrom = async (from: string, to: string, tokenId: string): Promise<string> => {
        return await this.contract.methods.transferFrom(from, to, tokenId).encodeABI()
    }
    approve = async (to: string, tokenId: string): Promise<string> => {
        return await this.contract.methods.approve(to, tokenId).encodeABI()
    }
    getApproved = async (tokenId: string): Promise<string> => {
        return await this.contract.methods.getApproved(tokenId).call()
    }
    setApprovalForAll = async (operator: string, approved: string): Promise<string> => {
        return await this.contract.methods.setApprovalForAll(operator, approved).encodeABI()
    }
    isApprovedForAll = async (owner: string, operator: string): Promise<boolean> => {
        return await this.contract.methods.isApprovedForAll(owner, operator).call()
    }

    safeTransferFromWithCallData(from: string, to: string, tokenId: string, data: string): Promise<string> {
        throw new Error("Method not implemented.");
    }

    decodeTransferFromParams = async (input:string):Promise<any>=>{
        const abi = {
            "inputs": [
                {
                    "internalType": "address",
                    "name": "from",
                    "type": "address"
                },
                {
                    "internalType": "address",
                    "name": "to",
                    "type": "address"
                },
                {
                    "internalType": "uint256",
                    "name": "tokenId",
                    "type": "uint256"
                }
            ],
            "name": "transferFrom",
            "outputs": [],
            "stateMutability": "nonpayable",
            "type": "function"
        }
        const signName = web3.eth.abi.encodeFunctionSignature(abi)
        if(signName != input.slice(0,10)){
            return ""
        }
        const rest = await web3.eth.abi.decodeParameters(abi["inputs"],"0x"+input.slice(10))
        return rest;
    }

    /**
     * @dev Returns the token collection name.
     */
    name = async (): Promise<string> => {
        return await this.contract.methods.name().call()
    }
    /**
     * @dev Returns the token collection symbol.
     */
    symbol = async (): Promise<string> => {
        return await this.contract.methods.symbol().call()
    }
    /**
     * @dev Returns the Uniform Resource Identifier (URI) for `tokenId` token.
     */
    tokenURI = async (tokenId: string): Promise<string> => {
        return await this.contract.methods.tokenURI(tokenId).call()
    }

    /**
     * @dev Returns a token ID owned by `owner` at a given `index` of its token list.
     * Use along with {balanceOf} to enumerate all of ``owner``'s tokens.
     */
    tokenOfOwnerByIndex = async (owner: string, index: number): Promise<string> => {
        return await this.contract.methods.tokenOfOwnerByIndex(owner, index).call()
    }
    /**
     * @dev Returns a token ID at a given `index` of all the tokens stored by the contract.
     * Use along with {totalSupply} to enumerate all tokens.
     */
    tokenByIndex = async (index: string): Promise<string> => {
        return await this.contract.methods.tokenByIndex(index).call()
    }

}

export default Eth